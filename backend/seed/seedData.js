// ─── Seed Function ─────────────────────────────────────────────────────────
async function seed() {
  try {
    await mongoose.connect(MONGO_URI);
    console.log('✅ Connected to MongoDB');

    // Clear existing data
    await Promise.all([
      DsaProblem.deleteMany({}),
      SystemDesign.deleteMany({}),
      AiTopic.deleteMany({}),
      DailyTask.deleteMany({}),
      MonthlyPlan.deleteMany({}),
      Progress.deleteMany({}),
    ]);
    console.log('🧹 Cleared existing data');

    // Insert base data
    const dsaProblems   = await DsaProblem.insertMany(DSA_PROBLEMS);
    const sdTopics      = await SystemDesign.insertMany(SYSTEM_DESIGN);
    const aiTopics      = await AiTopic.insertMany(AI_TOPICS);
    const monthlyPlans  = await MonthlyPlan.insertMany(MONTHLY_PLANS);
    await Progress.create({ userId: 'default' });
    console.log(`✅ Inserted: ${dsaProblems.length} DSA | ${sdTopics.length} SD | ${aiTopics.length} AI | ${monthlyPlans.length} Monthly Plans`);

    // Auto-generate daily tasks covering all SD and AI topics
    const maxDay = Math.max(
      sdTopics.reduce((m, t) => Math.max(m, t.dayNumber || 0), 0),
      aiTopics.reduce((m, t) => Math.max(m, t.dayNumber || 0), 0),
      90
    );
    const dailyTasks = [];
    for (let day = 1; day <= maxDay; day++) {
      const p1 = dsaProblems.find(p => p.dayNumber === day);
      const p2 = dsaProblems.find(p => p.dayNumber === day && p.name !== p1?.name) ||
                 dsaProblems.find(p => p.dayNumber === Math.max(1, day - 1));
      const sd = sdTopics.find(s => s.dayNumber === day) ||
                 sdTopics[day % sdTopics.length];
      const ai = aiTopics.find(a => a.dayNumber === day) ||
                 aiTopics[day % aiTopics.length];
      const cs = CS_TOPICS[(day - 1) % CS_TOPICS.length];
      const be = BACKEND_TOPICS[(day - 1) % BACKEND_TOPICS.length];

      dailyTasks.push({
        dayNumber: day,
        dsaProblem1: p1?._id || dsaProblems[0]._id,
        dsaProblem2: p2?._id || dsaProblems[1]._id,
        systemDesignTopic: sd._id,
        aiTopic: ai._id,
        backendTopic: be,
        csTopic: cs,
      });
    }
    await DailyTask.insertMany(dailyTasks);
    console.log(`✅ Generated ${dailyTasks.length} daily tasks`);
    console.log('\n🎉 Seed complete! Run `npm run dev` to start the server.\n');
    process.exit(0);
  } catch (err) {
    console.error('❌ Seed error:', err);
    process.exit(1);
  }
}

seed();
